package Functional_Programming;

public class List_Length {
    public static void main(String[] args) {
        
    int len=0;
    int[] array = new int[] {12,4,6,7,8,3,5};

    for(int i=0; i<array.length;i++){
         len +=1;

    }
    System.out.println("length of array is: "+len);
}
    
}
